#include <pgserver/common.h>
#include <pgserver/init.h>
#include <pgserver/os.h>

int main(int argc, char **argv) {
  g_error e;

  e = pgserver_main(0,argc,argv);
  if (iserror(e)) {
    os_show_error(e);
    return 1;
  }

  return os_child_returncode();
}

