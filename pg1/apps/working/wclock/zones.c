#include "wclock.h"
#include <stdint.h>

#define LINELEN    200
#define ENDLINE    0xff
#define ADD200     0xfe
#define ENDZONE    0xfd
#define REP        0xfc
#define OFFSET     201
#define REPEAT(n)  REP, n
#define TEXTPOS(x) (x)>>8, (x)&0xff

static unsigned char zones[]; /* forward proto */

enum { false=0, true=(0==0) };

typedef struct {
  int         hour;       /* offset to GMT in hours */
  int         min;        /* offset to GMT in minutes */
  int         delta_min;  /* hour*60 + min */
  int         text_pos;   /* pos of text in the bat below the map */
  int         index;      /* index of the x-offset in zones[] */
} ZoneDef;

static ZoneDef zone_def[NB_ZONES];
static int WIDTH, HEIGHT;

static pghandle timebar_txt_handle[NB_ZONES];

int needs_pgfx_update = 0;

/*****************************************************************************/

#define ABS(x) ( (x)>=0 ? (x) : -(x) )

void
get_delta(int zone, int* sign, int* hour, int* min)
{
  ENTER("get_delta(int zone, int* sign, int* hour, int* min)");
  if(zone<NB_ZONES && zone>=0) {
    ZoneDef *zd = &zone_def[zone];
    *sign = zd->hour >= 0 ? 1 : -1;
    *hour = ABS(zd->hour);
    *min = ABS(zd->min);
  }
  else {
    *sign = *hour = *min = 0;
  }
  LEAVE;
}

/*****************************************************************************/

static inline int
uc2i(unsigned char c)
{
  if(c<128) return c;
  return c - 256;
}

/*****************************************************************************/

static int
register_zone(int ix, ZoneDef* zd)
{
  ENTER("register_zone(int ix, ZoneDef* zd)");
  unsigned char a,b;

  zd->hour      = uc2i(zones[ix++]);
  zd->min       = uc2i(zones[ix++]);
  zd->delta_min = zd->hour * 60 + zd->min;
  a             = zones[ix++];
  b             = zones[ix++];
  zd->text_pos  = (int)(int16_t) ( (a<<8) | (b) );
  zd->index     = ix;

  ix++; /* skip x offset */

  while( zones[ix]!=ENDZONE ) ++ix;

  LEAVE;
  return ix+1;
}

/*****************************************************************************/

void
draw_zone(int nr)
{
  ENTER("draw_zone(int nr)");
  int ix = zone_def[nr].index;
  int x = 0;
  int y;
  int newx = 0;
  int draw = false;
  
  int offset = zones[ix++];
  int repeat = 1;
  
  TRACEF(">>>draw_zone(%d) %d %d\n", nr, HEIGHT, WIDTH);

  for(y=0, x=offset; y<HEIGHT;) {
    switch(zones[ix]) {
    case ENDZONE:
      goto done;
    case ADD200:
      x += LINELEN+1;
      break;
    case ENDLINE:
      ++y;
      x = offset;
      draw = false;
      break;
    case REP:
      ++ix;
      repeat = zones[ix];
      break;
    default:
      newx = x + zones[ix];
      if(draw) {
	int j;
	for(j=0; j<repeat; ++j) {
	  line(x, y, newx-1,y);
	  ++y;
	  if(y-1>=HEIGHT) break;
	}
	--y;
	repeat = 1;
      }
      draw = !draw;
      x = newx;
    }
    ++ix;
  }
 done:
  needs_pgfx_update = 1;
  LEAVE;
}

/*****************************************************************************/

void
init_zones()
{
  ENTER("init_zones()");
  int i;
  int index = 0;

  HEIGHT = get_world_height();
  WIDTH = get_world_width();


  TRACEF(">>>init_zones()\n");

  for(i=0; i<NB_ZONES; ++i) {
    timebar_txt_handle[i] = 0;
    index = register_zone(index, &zone_def[i]);
    DPRINTF("Zone %d: h=%d m=%d dm=%d ix=%d\n", 
	   i,
	   zone_def[i].hour,
	   zone_def[i].min,
	   zone_def[i].delta_min,
	   zone_def[i].index
	   );
  }
  LEAVE;
}

/*****************************************************************************/

static int
is_in_zone(int nr, int px, int py)
{
  ENTER("is_in_zone(int nr, int px, int py)");
  int ix = zone_def[nr].index;
  int x = 0;
  int y;
  int newx = 0;
  int draw = false;
  
  int offset = zones[ix++];
  int repeat = 1;
  
  TRACEF(">>>is_in_zone(%d, %d, %d)\n", nr, px, py);

  for(y=0, x=offset; y<HEIGHT;) {
    switch(zones[ix]) {
    case ENDZONE:
      goto done;
    case ADD200:
      x += LINELEN+1;
      break;
    case ENDLINE:
      ++y;
      if(y>py) {
	LEAVE;
	return 0;
      }
      x = offset;
      draw = false;
      break;
    case REP:
      ++ix;
      repeat = zones[ix];
      break;
    default:
      newx = x + zones[ix];
      if(draw) {
	int j;
	for(j=0; j<repeat; ++j) {
          if(py==y && px>=x && px<newx) {
	    LEAVE;
	    return 1;
	  }
	  ++y;
	  if(y-1>=HEIGHT) break;
	}
	--y;
	if(y>py) {
	  LEAVE;
	  return 0;
	}
	repeat = 1;
      }
      draw = !draw;
      x = newx;
    }
    ++ix;
  }
 done:
  LEAVE;
  return 0;
}

/*****************************************************************************/

static int
get_text_zone(int x)
{
  ENTER("get_text_zone(int x)");
  int i;
  int index;
  int pos, end_pos;

  TRACEF(">>>get_text_zone()\n");

  /* find zone with left border at 0 */
  for(index=0; index<NB_ZONES; ++index) {
    end_pos = zone_def[index].text_pos;
    if(end_pos==0) break;
  }

  for(i=0; i<NB_ZONES-1; ) {
    int next = index;
    pos = end_pos;

    do {
      ++i;
      if(++next>=NB_ZONES) next = 0;
      end_pos = zone_def[next].text_pos;
    } while(end_pos==-1);

    //if(x>start_pos) return index;
    //if(start_pos<pos && x>start_pos && x<pos) return index-1;
    if(pos<end_pos && x>=pos && x<end_pos) {
      LEAVE;
      return index;
    }

    index = next;
  }
  LEAVE;
  return -1;  
}

/*****************************************************************************/

int
get_zone(int x, int y)
{
  ENTER("get_zone(int x, int y)");
  int i;

  TRACEF(">>>get_zone(int x, int y)\n");

  if(y>=HEIGHT) {
    int ret = get_text_zone(x);
    LEAVE;
    return ret;
  }

  for(i=0; i<NB_ZONES; ++i) {
    if(is_in_zone(i, x, y)) {
      LEAVE;
      return i;
    }
  }

  LEAVE;
  return -1;
}

/*****************************************************************************/

void 
draw_timebar(int gmt_h, int current_nr, int with_text)
{
  ENTER("draw_timebar(int gmt_h, int current_nr, int with_text)");
  int i;
  int index;
  int pos, end_pos;
  int alt = 0;

  TRACEF(">>>draw_timebar(%d,%d,%d)\n", gmt_h, current_nr, with_text);

  for(index=0; index<NB_ZONES; ++index) {
    end_pos = zone_def[index].text_pos;
    if(end_pos==0) break;
  }

  set_color(FLAT);
  rectangle(0, HEIGHT-1, WIDTH, TIMEBAR_HEIGHT);
  TRACEF(" >>R: %d %d %d %d\n", 0, HEIGHT, WIDTH-1, HEIGHT-1);

  for(i=0; i<NB_ZONES-1; ) {
    int next = index;
    int is_current;
    pghandle pgstr;
    pos = end_pos;

    is_current = (index == current_nr);

    do {
      ++i;
      if(++next>=NB_ZONES) next = 0;
      end_pos = zone_def[next].text_pos;
    } while(end_pos==-1);

    DPRINTF(">>%d: %d .. %d [%d]\n", i, pos, end_pos, is_current); //getchar();
    if(!is_current) {
      set_color(is_current ? SHADOW : ILLUM);
      line(pos, HEIGHT, pos, HEIGHT+TIMEBAR_HEIGHT-1);
      line(pos, HEIGHT, end_pos-1, HEIGHT);
      
      set_color(is_current ? ILLUM : SHADOW);
      line(end_pos-1, HEIGHT, end_pos-1, HEIGHT+TIMEBAR_HEIGHT-1);
      line(pos+1, HEIGHT+TIMEBAR_HEIGHT-1, end_pos-1, HEIGHT+TIMEBAR_HEIGHT-1);
    }
    if(pos<end_pos) {
      char buf[20];
      sprintf(buf, "%02d", (gmt_h + zone_def[index].hour +24)%24 );
      set_color(!is_current ? WHITE : BLACK);
      //pgSetAngle(gfx_context, 90);
      //pgText(gfx_context, pos-1, HEIGHT+TIMEBAR_HEIGHT-3, pgNewString(buf));
      alt = !alt;

      if(timebar_txt_handle[i]) pgDelete(timebar_txt_handle[i]);
      pgstr = timebar_txt_handle[i] = pgNewString(buf);
      pgText(gfx_context, pos-2, HEIGHT-1 + alt*7, pgstr);
      if(is_current) {
	pgText(gfx_context, pos-1, HEIGHT-1 + alt*7, pgstr);
	}
      pgSetAngle(gfx_context, 0);
    }
    index = next;
  }

  needs_pgfx_update = 1;
  LEAVE;
}

